    private int getinNo(){
        int id=0;
        try {
            dbconn.connect();
            ResultSet rs=dbconn.stm.executeQuery("select customer_id from customer");
            while(rs.next())
                 id=rs.getInt("customer_id");
            dbconn.discoonect();
            id++;
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(billpage.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(billpage.class.getName()).log(Level.SEVERE, null, ex);
        }
        return id;
    }
    
