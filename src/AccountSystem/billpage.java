   private void gettabdata(){
        DefaultTableModel mod=(DefaultTableModel)jt1.getModel();
        int i=0;
        while(i<mod.getRowCount()){
            String pro=String.valueOf(mod.getValueAt(i,0));
            String qu=String.valueOf(mod.getValueAt(i, 1));
            String qt=String.valueOf(mod.getValueAt(i, 2));
            String pri=String.valueOf(mod.getValueAt(i, 3));
            String tax=String.valueOf(mod.getValueAt(i, 4));
            String gst=String.valueOf(mod.getValueAt(i, 5));
            String dis=String.valueOf(mod.getValueAt(i, 6));
            String tot=String.valueOf(mod.getValueAt(i, 7));
            try {
                dbconn.stm.execute("insert into product values('"+invno+"','"+pro+"','"+qu+"','"+qt+"','"+pri+"','"+tax+"','"+gst+"','"+dis+"','"+tot+"')");
            } catch (SQLException ex) {
                Logger.getLogger(billpage.class.getName()).log(Level.SEVERE, null, ex);
            }
            i++;
        }
        
    }
